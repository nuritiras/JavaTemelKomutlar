public class AtamaOperatorleri {
    public static void main(String[] args) {
        //8. UYGULAMA:
        int x = 15;
        System.out.println("x = " + x);
        x += 3;
        System.out.println("x += 3 : " + x);
        x -= 2;
        System.out.println("x -= 2 : " + x);
        x *= 2;
        System.out.println("x *= 2 : " + x);
        x /= 4;
        System.out.println("x /= 4 : " + x);
        x %= 2;
        System.out.println("x %= 2 : " + x);

        //SIRA SÄ°ZDE
        x = 13;
        System.out.println("x = " + x);
        x += 3;
        System.out.println("x += 3 : " + x);
        x -= 2;
        System.out.println("x -= 2 : " + x);
        x *= 2;
        System.out.println("x *= 2 : " + x);
        x /= 4;
        System.out.println("x /= 4 : " + x);
        x %= 2;
        System.out.println("x %= 2 : " + x);
    }
}
